def optimize_image(img_bytes_io: BytesIO, max_size=1024, quality=85):
    """–û–ø—Ç–∏–º–∏–∑–∏—Ä—É–µ—Ç –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ: –∏–∑–º–µ–Ω—è–µ—Ç —Ä–∞–∑–º–µ—Ä, –∫–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ—Ç –≤ RGB, —Å–∂–∏–º–∞–µ—Ç."""
    try:
        img_bytes_io.seek(0) # –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ–º —á—Ç–µ–Ω–∏–µ —Å –Ω–∞—á–∞–ª–∞
        img = Image.open(img_bytes_io)
        original_format = img.format
        logger.info(f"–û–ø—Ç–∏–º–∏–∑–∞—Ü–∏—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è: –Ω–∞—á–∞–ª—å–Ω—ã–π —Ä–∞–∑–º–µ—Ä {img.size}, —Ñ–æ—Ä–º–∞—Ç {original_format}, —Ä–µ–∂–∏–º {img.mode}")

        if img.mode not in ['RGB', 'L']: # L - –¥–ª—è —á–µ—Ä–Ω–æ-–±–µ–ª—ã—Ö
            if img.mode == 'RGBA' or img.mode == 'LA' or (img.mode == 'P' and 'transparency' in img.info):
                logger.info(f"–ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –∏–∑ —Ä–µ–∂–∏–º–∞ {img.mode} –≤ RGB –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è –∞–ª—å—Ñ–∞-–∫–∞–Ω–∞–ª–∞.")
                img = img.convert('RGB')
            elif img.mode == 'P': # –ü–∞–ª–∏—Ç—Ä–æ–≤—ã–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –±–µ–∑ —è–≤–Ω–æ–π –ø—Ä–æ–∑—Ä–∞—á–Ω–æ—Å—Ç–∏
                 logger.info(f"–ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è –ø–∞–ª–∏—Ç—Ä–æ–≤–æ–≥–æ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è (—Ä–µ–∂–∏–º {img.mode}) –≤ RGB.")
                 img = img.convert('RGB')
            else: # –î—Ä—É–≥–∏–µ —Ä–µ–∂–∏–º—ã, –Ω–∞–ø—Ä–∏–º–µ—Ä CMYK, –∫–æ—Ç–æ—Ä—ã–µ –º–æ–≥—É—Ç –±—ã—Ç—å –Ω–µ–∂–µ–ª–∞—Ç–µ–ª—å–Ω—ã
                logger.warning(f"–ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –≤ —Ä–µ–∂–∏–º–µ {img.mode}. –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–∞—è –∫–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è –≤ RGB.")
                img = img.convert('RGB')

        if img.width > max_size or img.height > max_size:
            img.thumbnail((max_size, max_size), Image.Resampling.LANCZOS)
            logger.info(f"–ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∏–∑–º–µ–Ω–µ–Ω–æ –¥–æ —Ä–∞–∑–º–µ—Ä–∞ {img.size}")

        try: # ImageOps –º–æ–∂–µ—Ç –∏–Ω–æ–≥–¥–∞ –≤—ã–∑—ã–≤–∞—Ç—å –æ—à–∏–±–∫–∏ –Ω–∞ —Å–ø–µ—Ü–∏—Ñ–∏—á–Ω—ã—Ö —Ñ–∞–π–ª–∞—Ö
            img = ImageOps.autocontrast(img, cutoff=0.5) # –î–æ–±–∞–≤–∏–ª cutoff –¥–ª—è –±–æ–ª–µ–µ –º—è–≥–∫–æ–π –∞–≤—Ç–æ–∫–æ–Ω—Ç—Ä–∞—Å—Ç–Ω–æ—Å—Ç–∏
        except Exception as e_ops:
            logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–∏–º–µ–Ω–∏—Ç—å –∞–≤—Ç–æ–∫–æ–Ω—Ç—Ä–∞—Å—Ç: {e_ops}. –ü—Ä–æ–¥–æ–ª–∂–∞–µ–º –±–µ–∑ –Ω–µ–≥–æ.")


        optimized_buffer = BytesIO()
        img.save(optimized_buffer, format='JPEG', quality=quality, optimize=True)
        optimized_bytes = optimized_buffer.getvalue()
        
        logger.info(f"–ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –æ–ø—Ç–∏–º–∏–∑–∏—Ä–æ–≤–∞–Ω–æ: –∏—Ç–æ–≥–æ–≤—ã–π —Ä–∞–∑–º–µ—Ä —Ñ–∞–π–ª–∞ {len(optimized_bytes) / 1024:.1f} KB")
        return optimized_bytes
    except Exception as e:
        logger.error(f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ–ø—Ç–∏–º–∏–∑–∞—Ü–∏–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è: {e}", exc_info=True)
        # –í —Å–ª—É—á–∞–µ —Å–µ—Ä—å–µ–∑–Ω–æ–π –æ—à–∏–±–∫–∏ –æ–ø—Ç–∏–º–∏–∑–∞—Ü–∏–∏, –ª—É—á—à–µ –≤–µ—Ä–Ω—É—Ç—å –æ—à–∏–±–∫—É, —á–µ–º –ø—ã—Ç–∞—Ç—å—Å—è –æ—Ç–ø—Ä–∞–≤–∏—Ç—å "—Å—ã—Ä—ã–µ" –¥–∞–Ω–Ω—ã–µ
        raise ValueError(f"–ù–µ —É–¥–∞–ª–æ—Å—å –æ–ø—Ç–∏–º–∏–∑–∏—Ä–æ–≤–∞—Ç—å –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ: {e}")


